#!/usr/bin/env node
// scripts/gen-cycle-docs.mjs
// C-339 PR-C item 1: kill the hand-edited cycle-counter drift surface.
//
// CURRENT_CYCLE.md / CURRENT_STATE.md historically carried a hand-typed
// "## Cycle / C-xxx" header that froze (CURRENT_CYCLE was stuck at C-326,
// CURRENT_STATE at C-278) while the live cycle moved on — the same class of
// drift that froze canon at C-288.
//
// Fix: the volatile runtime values (cycle, GI, mode, provenance, freshness)
// are now GENERATED from the authoritative published pulse
// (ledger/cycle-state.json, written by .github/workflows/publish-cycle-state.yml
// from /api/terminal/snapshot-lite) into a managed block. Hand-authored
// doctrine below the block is preserved untouched.
//
// Usage:
//   node scripts/gen-cycle-docs.mjs           # rewrite managed blocks in place
//   node scripts/gen-cycle-docs.mjs --check    # exit 1 if any file is stale
//
// CC0 Public Domain.

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const STATE_PATH = join(ROOT, 'ledger', 'cycle-state.json');

const BEGIN = '<!-- MOBIUS:CYCLE-STATE:BEGIN (generated by scripts/gen-cycle-docs.mjs — do not hand-edit) -->';
const END = '<!-- MOBIUS:CYCLE-STATE:END -->';

const SNAPSHOT_URL = 'https://mobius-civic-ai-terminal.vercel.app/api/terminal/snapshot-lite';

function loadState() {
  let raw;
  try {
    raw = readFileSync(STATE_PATH, 'utf8');
  } catch (err) {
    console.error(`❌ cannot read ${STATE_PATH}: ${err.message}`);
    console.error('   This file is published by the publish-cycle-state workflow.');
    process.exit(1);
  }
  const state = JSON.parse(raw);
  if (typeof state.cycle !== 'string' || state.cycle.length === 0) {
    console.error('❌ ledger/cycle-state.json has no "cycle" — refusing to generate.');
    process.exit(1);
  }
  return state;
}

// Build the managed block. Operator truth only: every value is sourced from the
// published pulse and labelled with its provenance + freshness. No invented data.
function renderBlock(state) {
  const provenance =
    `\`${state.gi_provenance ?? 'unknown'}\`` +
    (state.gi_verified === false ? ' (unverified)' : state.gi_verified ? ' (verified)' : '');
  const bullets = [
    `- **Cycle:** \`${state.cycle}\``,
    `- **GI:** \`${state.gi ?? 'n/a'}\` · **mode:** \`${state.mode ?? 'n/a'}\`` +
      (state.degraded ? ' · ⚠️ degraded' : ''),
    `- **Provenance:** ${provenance}`,
    `- **Pulse fetched:** \`${state.fetched_at ?? 'unknown'}\``,
  ];
  if (state.deployment?.commit_sha) {
    bullets.push(
      `- **Deployment:** \`${state.deployment.commit_sha.slice(0, 12)}\` (${state.deployment.environment ?? 'unknown'})`,
    );
  }
  if (state.hot?.seals_raw != null || state.cold?.manifest_blocks != null) {
    bullets.push(
      `- **Reserve hot (raw):** \`${state.hot?.seals_raw ?? 'n/a'}\` · **cold manifest:** \`${state.cold?.manifest_blocks ?? 'n/a'}\` · **gap (raw−cold):** \`${state.cold?.gap_raw_vs_cold ?? 'n/a'}\``,
    );
    if (state.hot?.chain_tip?.seal_id) {
      bullets.push(
        `- **Chain tip:** \`${state.hot.chain_tip.seal_id}\` (seq \`${state.hot.chain_tip.sequence ?? 'n/a'}\`)`,
      );
    }
  }
  if (Array.isArray(state.open_gates) && state.open_gates.length > 0) {
    bullets.push(`- **Open gates:** ${state.open_gates.map((g) => `\`${g}\``).join(', ')}`);
  }
  return [
    BEGIN,
    '',
    ...bullets,
    '',
    `> Generated from \`ledger/cycle-state.json\` (source: \`${state.source ?? 'snapshot-lite'}\`).`,
    `> Live source of truth: [snapshot-lite](${SNAPSHOT_URL}). Do not hand-edit this block.`,
    '',
    END,
  ].join('\n');
}

// Replace an existing managed block, or insert one right after the first H1.
function applyBlock(content, block) {
  const beginIdx = content.indexOf(BEGIN);
  const endIdx = content.indexOf(END);
  if (beginIdx !== -1 && endIdx !== -1 && endIdx > beginIdx) {
    const before = content.slice(0, beginIdx);
    const after = content.slice(endIdx + END.length);
    return `${before}${block}${after}`;
  }
  // No block yet: insert after the first markdown H1 line.
  const lines = content.split('\n');
  const h1 = lines.findIndex((l) => /^#\s+/.test(l));
  if (h1 === -1) {
    return `${block}\n\n${content}`;
  }
  lines.splice(h1 + 1, 0, '', block, '');
  return lines.join('\n');
}

const TARGETS = ['CURRENT_CYCLE.md', 'CURRENT_STATE.md'];

function main() {
  const check = process.argv.includes('--check');
  const state = loadState();
  const block = renderBlock(state);

  let stale = false;
  for (const name of TARGETS) {
    const path = join(ROOT, name);
    let current;
    try {
      current = readFileSync(path, 'utf8');
    } catch {
      console.error(`❌ ${name} not found at repo root.`);
      process.exit(1);
    }
    const next = applyBlock(current, block);
    if (next === current) {
      console.log(`✓ ${name} up to date (${state.cycle}).`);
      continue;
    }
    if (check) {
      console.error(`❌ ${name} cycle header is stale — run: node scripts/gen-cycle-docs.mjs`);
      stale = true;
    } else {
      writeFileSync(path, next, 'utf8');
      console.log(`✅ ${name} regenerated for ${state.cycle}.`);
    }
  }

  if (check && stale) process.exit(1);
}

main();
