#!/usr/bin/env node
// scripts/gen-route-manifest.mjs
// C-339 PR-C items 11 + 12: generate docs/ROUTE_MANIFEST.md from app/**/route.ts.
//
// 210 API routes with no index is an unauditable surface. This produces a
// deterministic, sorted manifest of every route, the HTTP methods it exports,
// and its declared tier (item 12 convention: `export const tier = '...'`).
// Untiered routes are listed so the surface can be classified over time.
//
// Usage:
//   node scripts/gen-route-manifest.mjs           # write docs/ROUTE_MANIFEST.md
//   node scripts/gen-route-manifest.mjs --check    # exit 1 if manifest is stale
//
// Tier vocabulary (item 12): public | authed | agent-gateway | cron | internal.
// CC0 Public Domain.

import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import { join, dirname, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const APP_DIR = join(ROOT, 'app');
const OUT = join(ROOT, 'docs', 'ROUTE_MANIFEST.md');
const TIER_MAP_PATH = join(ROOT, 'docs', 'route-tiers.json');

const HTTP_METHODS = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE', 'HEAD', 'OPTIONS'];
const KNOWN_TIERS = ['public', 'authed', 'agent-gateway', 'cron', 'internal'];

function walk(dir, acc = []) {
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) walk(full, acc);
    else if (name === 'route.ts' || name === 'route.tsx') acc.push(full);
  }
  return acc;
}

function detectMethods(src) {
  const found = [];
  for (const m of HTTP_METHODS) {
    const re = new RegExp(
      `export\\s+(?:async\\s+)?function\\s+${m}\\b|export\\s+const\\s+${m}\\s*[:=]`,
    );
    if (re.test(src)) found.push(m);
  }
  return found;
}

function detectTier(src) {
  const m = /export\s+const\s+tier\s*(?::[^=]+)?=\s*['"]([a-z-]+)['"]/.exec(src);
  return m ? m[1] : null;
}

function routePath(file) {
  // app/api/foo/[id]/route.ts -> /api/foo/[id]
  const rel = relative(APP_DIR, dirname(file)).split(sep).join('/');
  return '/' + rel;
}

function loadTierMap() {
  try {
    const parsed = JSON.parse(readFileSync(TIER_MAP_PATH, 'utf8'));
    return parsed && typeof parsed.tiers === 'object' ? parsed.tiers : {};
  } catch {
    return {};
  }
}

function collect() {
  const tierMap = loadTierMap();
  const files = walk(APP_DIR);
  const rows = files.map((file) => {
    const src = readFileSync(file, 'utf8');
    const path = routePath(file);
    // Inline `export const tier` wins; central docs/route-tiers.json is the
    // fallback so the surface can be classified without churning 211 files.
    const tier = detectTier(src) ?? tierMap[path] ?? null;
    return { path, methods: detectMethods(src), tier };
  });
  rows.sort((a, b) => a.path.localeCompare(b.path));
  return rows;
}

function render(rows) {
  const untiered = rows.filter((r) => !r.tier);
  const badTier = rows.filter((r) => r.tier && !KNOWN_TIERS.includes(r.tier));
  const lines = [];
  lines.push('# Route Manifest');
  lines.push('');
  lines.push('> Generated by `scripts/gen-route-manifest.mjs` — do not hand-edit.');
  lines.push('> Regenerate after adding/removing routes: `node scripts/gen-route-manifest.mjs`.');
  lines.push('');
  lines.push(`- **Total routes:** ${rows.length}`);
  lines.push(`- **Tiered:** ${rows.length - untiered.length} · **untiered:** ${untiered.length}`);
  lines.push(`- **Tier vocabulary (item 12):** \`${KNOWN_TIERS.join('` · `')}\``);
  lines.push('');
  lines.push('Declare a tier in a route with: `export const tier = \'public\';`');
  lines.push('');
  lines.push('| Route | Methods | Tier |');
  lines.push('| --- | --- | --- |');
  for (const r of rows) {
    const methods = r.methods.length ? r.methods.join(', ') : '—';
    const tier = r.tier ?? '`untiered`';
    lines.push(`| \`${r.path}\` | ${methods} | ${tier} |`);
  }
  lines.push('');
  if (badTier.length > 0) {
    lines.push('## ⚠️ Unknown tier values');
    lines.push('');
    for (const r of badTier) lines.push(`- \`${r.path}\` → \`${r.tier}\` (not in vocabulary)`);
    lines.push('');
  }
  return { text: lines.join('\n') + '\n', untiered, badTier };
}

function main() {
  const check = process.argv.includes('--check');
  const rows = collect();
  const { text, untiered, badTier } = render(rows);

  if (badTier.length > 0) {
    console.error(`❌ ${badTier.length} route(s) declare a tier outside the vocabulary:`);
    for (const r of badTier) console.error(`   ${r.path} -> ${r.tier}`);
    process.exit(1);
  }

  if (check) {
    let current = '';
    try {
      current = readFileSync(OUT, 'utf8');
    } catch {
      /* missing -> stale */
    }
    if (current !== text) {
      console.error('❌ docs/ROUTE_MANIFEST.md is stale — run: node scripts/gen-route-manifest.mjs');
      process.exit(1);
    }
    console.log(`✅ route manifest up to date (${rows.length} routes, ${untiered.length} untiered).`);
    return;
  }

  writeFileSync(OUT, text, 'utf8');
  console.log(`✅ wrote docs/ROUTE_MANIFEST.md (${rows.length} routes, ${untiered.length} untiered).`);
}

main();
